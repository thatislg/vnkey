// ==================== Hàm tra cứu ====================

use once_cell::sync::Lazy;
use std::collections::HashMap;

/// Bảng tra sắp xếp: mã Unicode → chỉ mục StdVnChar
static UNICODE_LOOKUP: Lazy<HashMap<u16, usize>> = Lazy::new(|| {
    let mut map = HashMap::new();
    for (i, &code) in UNICODE_TABLE.iter().enumerate() {
        map.entry(code).or_insert(i);
    }
    map
});

/// Bảng tra composite (dạng tách rời) Unicode
static COMPOSITE_LOOKUP: Lazy<HashMap<u32, usize>> = Lazy::new(|| {
    let mut map = HashMap::new();
    // Thêm mục composite
    for (i, &comp) in UNICODE_COMPOSITE.iter().enumerate() {
        map.entry(comp).or_insert(i);
    }
    // Cũng thêm mục precomposed (từ UNICODE_TABLE) khác biệt
    for (i, &code) in UNICODE_TABLE.iter().enumerate() {
        let comp_val = UNICODE_COMPOSITE[i];
        if (code as u32) != comp_val {
            map.entry(code as u32).or_insert(i);
        }
    }
    map
});

/// Tra mã Unicode và trả chỉ mục StdVnChar (0-212), hoặc None
pub fn unicode_to_index(code: u16) -> Option<usize> {
    UNICODE_LOOKUP.get(&code).copied()
}

/// Tra giá trị composite Unicode và trả chỉ mục StdVnChar (0-212), hoặc None
pub fn unicode_composite_to_index(composite: u32) -> Option<usize> {
    COMPOSITE_LOOKUP.get(&composite).copied()
}
